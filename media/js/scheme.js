    Station: {
        getByCoords: function (latitude, longitude) {
            for (var s in Scheme.Stations) {
                if (Scheme.Stations[s].areas === undefined) continue;
                for (var area in Scheme.Stations[s].areas) {
                    if (Scheme.Stations[s].areas[area].p2.latitude <= latitude && Scheme.Stations[s].areas[area].p1.latitude >= latitude && Scheme.Stations[s].areas[area].p1.longitude <= longitude && Scheme.Stations[s].areas[area].p2.longitude >= longitude) {
                        return Scheme.Stations[s];
                    }
                }
            }
            return false;
        }
    },
    Geo: {
        state: false,
        autocomplete: true,
        get: function () {
            if (navigator.geolocation) {
                navigator.geolocation.getCurrentPosition(this.isOk, this.isError, {enableHighAccuracy: true});
            }
        },
        isOk: function (position) {
            Scheme.Geo.state = true;
            window.localStorage.setItem('latitude', position.coords.latitude.toString());
            window.localStorage.setItem('longitude', position.coords.longitude.toString());
            if (Scheme.Select.from === 0 || Scheme.Select.to === 0) return;
            Scheme.Stat.send(position.coords.latitude, position.coords.longitude);
        },
        isError: function (code) {
            Scheme.Geo.state = false;
        },
    },
